//Math.trunc(x) return the integer part of x
const num1 = 4.9;
const num2 = 4.7;
const num3 = 4.4;
const num4 = 4.2;
const num5 = -4.2;
console.log(Math.trunc(num1));//4
console.log(Math.trunc(num2));//4
console.log(Math.trunc(num3));//4
console.log(Math.trunc(num4));//4
console.log(Math.trunc(num5));//-4